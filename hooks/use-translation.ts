export function useTranslation(initialLanguage?: string) {
  const [language, setLanguage] = useState<Language>("en")

  useEffect(() => {
    if (initialLanguage && LANGUAGES[initialLanguage as Language]) {
      setLanguage(initialLanguage as Language)
      localStorage.setItem("cropwise-language", initialLanguage)
    } else {
      const savedLanguage = localStorage.getItem("cropwise-language") as Language
      if (savedLanguage && LANGUAGES[savedLanguage]) {
        setLanguage(savedLanguage)
      }
    }
  }, [initialLanguage])

  const changeLanguage = (newLanguage: Language) => {
    setLanguage(newLanguage)
    localStorage.setItem("cropwise-language", newLanguage)
  }

  const t = (key: keyof typeof translations, params?: Record<string, string>): string => {
    let translation = translations[key]?.[language] || translations[key]?.en || key

    if (params) {
      Object.entries(params).forEach(([param, value]) => {
        translation = translation.replace(`{${param}}`, value)
      })
    }

    return translation
  }

  return {
    language,
    changeLanguage,
    t,
    languages: LANGUAGES,
  }
}
