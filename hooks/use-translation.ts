export function useTranslation(initialLanguage?: string) {
  const [language, setLanguage] = useState<Language>("en")

  useEffect(() => {
    // Determine language code from (1) explicit param, (2) cropwise-language key, (3) user data full name, fallback en
    const resolve = (): Language => {
      if (initialLanguage && LANGUAGES[initialLanguage as Language]) return initialLanguage as Language

      const code = localStorage.getItem('cropwise-language') as Language | null
      if (code && LANGUAGES[code]) return code

      // Try user data storing full name
      try {
        const raw = localStorage.getItem('cropwise-user-data')
        if (raw) {
          const parsed = JSON.parse(raw)
          const full = parsed.language
          if (typeof full === 'string') {
            const matchEntry = Object.entries(LANGUAGES).find(([k, v]) => v === full)
            if (matchEntry) return matchEntry[0] as Language
          }
        }
      } catch (e) {
        console.warn('Failed mapping full language name to code', e)
      }
      return 'en'
    }
    const detected = resolve()
    setLanguage(detected)
    localStorage.setItem('cropwise-language', detected)
    // Ensure user data holds full name form
    try {
      const raw = localStorage.getItem('cropwise-user-data')
      if (raw) {
        const parsed = JSON.parse(raw)
        const full = LANGUAGES[detected]
        if (parsed.language !== full) {
          parsed.language = full
          localStorage.setItem('cropwise-user-data', JSON.stringify(parsed))
        }
      }
    } catch {}
  }, [initialLanguage])

  const changeLanguage = (newLanguage: Language) => {
    setLanguage(newLanguage)
    localStorage.setItem('cropwise-language', newLanguage)
    const full = LANGUAGES[newLanguage]
    try {
      const raw = localStorage.getItem('cropwise-user-data')
      if (raw) {
        const parsed = JSON.parse(raw)
        if (parsed.language !== full) {
          parsed.language = full
          localStorage.setItem('cropwise-user-data', JSON.stringify(parsed))
        }
      }
    } catch (e) {
      console.warn('Failed to update full language in user data', e)
    }
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: newLanguage } }))
  }

  // Listen for language changes from other components
  useEffect(() => {
    const handleLanguageChange = (event: CustomEvent) => {
      const newLanguage = event.detail.language as Language
      if (newLanguage !== language && LANGUAGES[newLanguage]) {
        setLanguage(newLanguage)
      }
    }

    window.addEventListener('languageChanged', handleLanguageChange as EventListener)
    return () => {
      window.removeEventListener('languageChanged', handleLanguageChange as EventListener)
    }
  }, [language])

  const t = (key: keyof typeof translations, params?: Record<string, string>): string => {
    let translation = translations[key]?.[language] || translations[key]?.en || key

    if (params) {
      Object.entries(params).forEach(([param, value]) => {
        translation = translation.replace(`{${param}}`, value)
      })
    }

    return translation
  }

  return {
    language,
    changeLanguage,
    t,
    languages: LANGUAGES,
  }
}
